!----- PROTON ELECTRONIC STOPPING POWER
      SUBROUTINE PSTOPZ95(Z1,A1,Z2,A2,E,SE)
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
       INTEGER IZ1,IZ2
      !COMMON /SCOEF/m_SCOEF95(93,54)
! ---- VELOCITY PROPOTIONAL STOPPING BELOW VELOCITY PE0
      IZ1=Z1+0.000001
      IZ2=Z2+0.000001
      IF(E.GT.1.D4) THEN
         PE=LOG(E)/E
         SE=m_SCOEF95((IZ2-1)*54+17)+(m_SCOEF95((IZ2-1)*54+18)*PE)+  &
           (m_SCOEF95((IZ2-1)*54+19)*PE*PE)+(m_SCOEF95((IZ2-1)*54+20)/PE)
         RETURN
      END IF
      PE0=10.0
      PE=DMAX1(PE0,E)
      SL=(m_SCOEF95((IZ2-1)*54+9)*PE**m_SCOEF95((IZ2-1)*54+10))+m_SCOEF95((IZ2-1)*54+11)* &
         PE**m_SCOEF95((IZ2-1)*54+12)
      SH=m_SCOEF95((IZ2-1)*54+13)/PE**m_SCOEF95((IZ2-1)*54+14)*DLOG((m_SCOEF95((IZ2-1)*54+15)/PE)+ &
         m_SCOEF95((IZ2-1)*54+16)*PE)
      SE=SL*SH/(SL+SH)
      IF(E.GT.PE0) GOTO 110
! ---- VELPWR IS THE POWER OF VELOCITY STOPPING BELOW PE0
      VELPWR=0.45
      IF(IZ2.LT.7) VELPWR=VELPWR-0.1
      SE=SE*(E/PE0)**VELPWR
  110 RETURN
      END SUBROUTINE PSTOPZ95
