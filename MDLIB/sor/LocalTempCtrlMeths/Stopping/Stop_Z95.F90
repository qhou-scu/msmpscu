!----- HEVAY ELECTRONIC STOPPING POWER
      SUBROUTINE HISTOPZ95(Z1,A1,Z2,A2,E,EE,SE)
      IMPLICIT DOUBLE PRECISION (A-Z)
      INTEGER IZ1,IZ2, J
! ---- USE VELOCITY STOPPING FOR (YRMIN=VR/Z1**0.67).LE.0.13
! ---- OR FOR VR.LE.1.0
      IZ1=Z1+0.000001
      IZ2=Z2+0.000001
      YRMIN=0.13
      VRMIN=1.00
      VFERMI= m_SCOEF95((IZ2-1)*54+7)
      V=DSQRT(E/25.0)/VFERMI
  100 IF(V.GE.1.0) GOTO 110
      VR=(3.0*VFERMI/4.0)*(1.0+(2.0*V**2/3.0)-V**4/15.0)
      GOTO 120
  110 VR=V*VFERMI*(1.0+1.0/(5.0*V**2))
! ---- SET YR =MAXIMUM OF (VR/Z1**0.67),(VRMIN/Z1**0.67) OR YRMIN
  120 YR=DMAX1(YRMIN,VR/Z1**0.6667)
      YR=DMAX1(YR,VRMIN/Z1**0.6667)
      A=-0.803*YR**0.3+1.3167*YR**0.6+0.38157*YR+0.008983*YR*YR
      Q=DMIN1(1.0D0,DMAX1(0.0D0,1.0-DEXP(-DMIN1(A,50.0D0))))
! ---- Q =IONIZATION LEVEL OF THE ION AT VELOCITY YR
! ---- NOW WE CONVERT IONIZATION LEVEL TO EFFECTIVE CHARGE
      DO J=22,39
       IF(Q.LE.m_SCOEF95((93-1)*54+J)) EXIT
      END DO
      J=J-1
      IF(J.LT.22) THEN
         J=22
      ELSE IF(J.GT.38) THEN
       J=38
      END IF
      RLAMBDA0=m_SCOEF95((IZ1-1)*54+J)
      RLAMBDA1=(Q-m_SCOEF95((93-1)*54+J))*(m_SCOEF95((IZ1-1)*54+J+1)- &
                m_SCOEF95((IZ1-1)*54+J))/(m_SCOEF95((93-1)*54+J+1)-m_SCOEF95((93-1)*54+J))
      RL=(RLAMBDA0+RLAMBDA1)/Z1**.33333
      ZETA0=Q+(1./(2.*VFERMI**2))*(1.-Q)*DLOG(1.+(4.*RL*VFERMI/1.919)**2)
      A=DLOG(E)
      IF(A.LT.0.0) A=0.D0
      ZETA=ZETA0*(1.+(1./Z1**2)*(.08+.0015*Z2)*DEXP(-(7.6-A)**2))
      A=VRMIN/Z1**.6667
      IF(A.LT.YRMIN) A=YRMIN
      IF(YR.LE.A) GOTO 130
      CALL PSTOPZ95(Z1,A1,Z2,A2,E,SP)
      SE=SP*(ZETA*Z1)**2
      !Add Fermi Velocity Correction - 1995
      !VFCORR is in SCOEF(Z2,41-54), Interpolation values in SCOEF(93,41-54)
      EION=E
      IF(EION.GT.9999) EION=9999         ! Not valid >1E4 keV/amu
      DO J=41,53
         IF(EION.LT.m_SCOEF95((93-1)*54+J)) EXIT
      END DO
      J=J-1
      IF(J.LT.41) THEN
       J=41
      ELSE IF(J.GT.53) THEN
       J=53
      END IF

      VFCORR0=m_SCOEF95((IZ2-1)*54+J)
      VFCORR1=(EION-m_SCOEF95((93-1)*54+J))*(m_SCOEF95((IZ2-1)*54+J+1)-m_SCOEF95((IZ2-1)*54+J))/&
               (m_SCOEF95((93-1)*54+J+1)-m_SCOEF95((93-1)*54+J))
      SE=SE*(VFCORR0+VFCORR1)
      RETURN

130   A=YRMIN*Z1**.6667
      IF(VRMIN.LT.A) VRMIN=A
      A=VRMIN**2-0.8*VFERMI**2
      IF(A.LT.0.0) A=0.0
      VMIN=.5*(VRMIN+DSQRT(A))
      EEE=25.0*VMIN**2
      CALL PSTOPZ95(Z1,A1,Z2,A2,EEE,SP)

      !Add Fermi Velocity Correction to Low Energy value ?
      ! VFCORR is in SCOEF(Z2,41-54), Interpolation values in SCOEF(93,41-54)
      EION=EEE
      IF(EION.GT.9999)EION=9999
      DO J=41,53
          IF(EION.LT.m_SCOEF95((93-1)*54+J)) EXIT
      END DO
      J=J-1
      IF(J.LT.41)  THEN
         J=41
      ELSE IF(J.GT.53) THEN
         J=53
      END IF
      VFCORR0=m_SCOEF95((IZ2-1)*54+J)
      VFCORR1=(EION-m_SCOEF95((93-1)*54+J))*(m_SCOEF95((IZ2-1)*54+J+1)-m_SCOEF95((IZ2-1)*54+J))/&
              (m_SCOEF95((93-1)*54+J+1)-m_SCOEF95((93-1)*54+J))
      SP=SP*(VFCORR0+VFCORR1)
      !  Following corrects for low-energy stopping, where little data exists.
      HIPOWER=.47                                       !TRIM-88 used 0.50
      IF(IZ1.EQ.3) THEN
         HIPOWER= 0.55
         GOTO 398
      END IF
      IF(Z2.LT.7) THEN
         HIPOWER= 0.375
         GOTO 398
      END IF
      ! Following compensates for semiconductor band-gap
      IF (IZ1.LT.18 .AND. (Z2.EQ.14 .OR. Z2.EQ.32) )THEN
          HIPOWER=0.375
      END IF
398   SE=(SP*(ZETA*Z1)**2)*(E/EEE)**HIPOWER

      RETURN
      END SUBROUTINE HISTOPZ95

